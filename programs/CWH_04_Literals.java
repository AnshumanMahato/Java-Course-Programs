package programs;

public class CWH_04_Literals {
    public static void main(String[] args) {
        byte age = 34;
        int age2 = 56;
        short age3 = 87;
        long ageDino = 566666666666666666L;
        char ch = 'A';
        float f1 = 5.6F;
        double d1 = 4.66D; // works without D or d as well
        System.out.println(age+" "+age2+" "+age3+" "+ageDino+" "+ch+" "+f1+" "+d1);    
    }

}